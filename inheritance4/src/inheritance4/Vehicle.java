package inheritance4;

public class Vehicle {
	private double price;
	private int year;

	public double getPrice() {
		return price;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public void setPrice(double price) {
		this.price = price;
	}
}
